drop database privateschool;
create database privateschool;
use privateschool;

create table student(
id int not null auto_increment,
first_name varchar(100) not null,
last_name varchar(100) not null,
dateofbirth date,
tuitionfees decimal(6,2),
primary key(id)
);
 
create table trainer(
id int not null auto_increment,
first_name varchar(100) not null,
last_name varchar(100) not null,
subject varchar(100) not null,
primary key(id)
);

create table course(
id int not null auto_increment,
title varchar(100) not null,
stream varchar(100) not null,
type varchar(100) not null,
start_date date,
end_date date,
primary key(id),
unique(title,stream,type)
);

create table assignment(
id int not null auto_increment,
title varchar(100) not null,
description varchar(2000),
oralmark decimal(5,2),
totalmark decimal(5,2),
primary key(id),
unique(title)
);

create table student_course(
student_id int not null,
course_id int not null,
primary key(student_id,course_id),
CONSTRAINT fk_student_course_student 
foreign key(student_id) references student(id),
CONSTRAINT fk_student_course_course
foreign key(course_id) references course(id)
);

create table trainer_course(
trainer_id int not null,
course_id int not null,
primary key(trainer_id,course_id),
constraint fk_trainer_course_trainer
foreign key(trainer_id) references trainer(id),
constraint fk_trainer_course_course
foreign key(course_id) references course(id)
);

create table course_assignment(
course_id int not null,
assignment_id int not null,
subdatetime timestamp,
primary key(course_id,assignment_id),
constraint fk_course_assignment_course
foreign key(course_id) references course(id),
constraint fk_course_assignment_assignment
foreign key(assignment_id) references assignment(id)
);

create table student_assignment(
student_id int not null,
assignment_id int not null,
oralmark decimal(5,2),
totalmark decimal(5,2),
primary key(student_id,assignment_id),
constraint fk_student_assignment_student
foreign key(student_id) references student(id),
constraint fk_student_assignment_assignment
 foreign key(assignment_id) references assignment(id)
);

